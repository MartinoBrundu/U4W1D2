package cartella1a;

public class Es2 {
    public static String stampaValore(int numero) {
        switch (numero) {
            case 0:
                return "zero";

            case 1:
                return"uno";

            case 2:
                return "due";

            case 3:
                return "tre";

            default:
                return "errore";

        }


    }


}
